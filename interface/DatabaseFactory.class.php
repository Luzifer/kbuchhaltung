class DatabaseFactory {
	public function GetDatabaseConnector($config) {
		switch($config['dbtype']) {
			case 'MySQL':
				include_once('connectors/DB_MySQL.class.php');
				$this->connector = new DB_MySQL();
		}
		
		return $this->connector;
	} 
}

